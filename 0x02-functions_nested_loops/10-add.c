#include "main.h"

/**
 * add - Adds two integers and returns the result.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The sum of the two integers.
 *
 * Description: This function takes two integers as input and adds them together.
 *              The result of the addition is returned as the function's return value.
 */
int add(int a, int b)
{
	int c;

	c = a + b;

	return (c);
}
